function init()
  connect(g_game, { onGameEnd = onGameEnd })
  connect(g_game, 'onTextMessage', onArena)
  
  kardWindow = g_ui.displayUI('voc')
  Escolher = kardWindow:recursiveGetChildById('Escolher')
  HP = kardWindow:recursiveGetChildById('hp')
  MP = kardWindow:recursiveGetChildById('mp')
  Cap = kardWindow:recursiveGetChildById('cap')
  Reg = kardWindow:recursiveGetChildById('reg')
  Escolher:disable()
  HP:hide()
  MP:hide()
  Cap:hide()
  Reg:hide()
  kardWindow:hide()
end

