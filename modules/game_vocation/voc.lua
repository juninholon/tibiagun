function hide()
  addEvent(function() g_effects.fadeOut(kardWindow, 250) end)
  scheduleEvent(function() kardWindow:hide() end, 250)
end

function toggle()
  if kardWindow:isVisible() then
    hide()
  else
    show()
  end
end