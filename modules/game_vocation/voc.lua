function onArena(mode, text)
	if not g_game.isOnline() then
		return
	end
	if mode == MessageModes.Failure then
		if string.find(text, "OpenedWindowVoc") then
		  kardWindow:show()
		  kardWindow:raise()
		  kardWindow:focus()
		  addEvent(function() g_effects.fadeIn(kardWindow, 250) end)
		end
	end
end

function terminate()
  disconnect(g_game, { onGameEnd = onGameEnd })
  disconnect(g_game, 'onTextMessage', onArena)

  kardWindow:destroy()
end

function onGameEnd()
  if kardWindow:isVisible() then
    kardWindow:hide()
  end
end

